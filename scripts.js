var generateBtn = document.querySelector('button.generate');
var quoteDisplay = document.querySelector('blockquote .quote');

generateBtn.addEventListener('click', function(){
    let quote = generateQuote(quoteArray);
    quoteDisplay.textContent = quote; // ASSIGN THE NEW QUOTE
}, false);

document.addEventListener('DOMContentLoaded', function(){
    generateBtn.click();
})

function generateQuote(quoteArray) {
    let newQuote = "";
    let existingQuote = document.querySelector('blockquote .quote').textContent;

    /* PICK NEW QUOTE, CANNOT BE THE SAME QUOTE AGAIN */
    do {
        let randomIndex = Math.floor(Math.random() * +quoteArray.length);
        newQuote = quoteArray[randomIndex];
    } while (newQuote == existingQuote);

    return newQuote;
}